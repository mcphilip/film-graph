package com.philipmcclelland.filmgraph

class InitializationService {
    ///////////////////////////////////////////////////
    //  Dependency Injections
    ///////////////////////////////////////////////////

    def afiTop100Service
    def myMovieAPIService
    def releaseDateService

    ///////////////////////////////////////////////////
    //  Properties
    ///////////////////////////////////////////////////

    static transactional = false

    ///////////////////////////////////////////////////
    //  Public Methods
    ///////////////////////////////////////////////////

    public void initialize() {
        log.debug("[initialize()] params: []")

        afiTop100Service.initializeAfiTop100Movies()
        myMovieAPIService.loadAPIDataForAfiTop100Movies()
        releaseDateService.initialize()
    }

    ///////////////////////////////////////////////////
    //  Private Methods
    ///////////////////////////////////////////////////
}